print("Hello from prj1.py")  # 這是把字串印出的指令

# 基本型態 basic types
print(1)  # 整數 int
print(1.1)  # 浮點數 float,只要有小數點就是浮點數
print("Hello")  # 字串 str,用双引號""或者單引號''包起來的都是字串
print(True)  # 布林值 bool,第一個字母大寫
print(None)  # 空值 NoneType

# 變數 variable
a = 1  # 把1這個整數存到電腦的記憶體中
# = 的功能是將右邊的資料賦給左邊的變數空間當中
print(a)  # 印出變數的值
